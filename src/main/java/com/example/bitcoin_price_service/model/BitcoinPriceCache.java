package com.example.bitcoin_price_service.model;

import jakarta.persistence.*;
import lombok.AllArgsConstructor;
import lombok.NoArgsConstructor;

import java.time.LocalDate;

@Entity
@AllArgsConstructor
@NoArgsConstructor
public class BitcoinPriceCache {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY) // This will auto-generate the ID
    private Long id;

    @Column(nullable = false)
    private LocalDate startDate;

    @Column(nullable = false)
    private LocalDate endDate;

    @Column(nullable = false)
    private String currency;

    @Column(nullable = false, columnDefinition = "TEXT")
    private String jsonData;

    @Column(nullable = false)
    private LocalDate cachedAt;

    // Getters and Setters

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public LocalDate getStartDate() {
        return startDate;
    }

    public void setStartDate(LocalDate startDate) {
        this.startDate = startDate;
    }

    public LocalDate getEndDate() {
        return endDate;
    }

    public void setEndDate(LocalDate endDate) {
        this.endDate = endDate;
    }

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }

    public String getJsonData() {
        return jsonData;
    }

    public void setJsonData(String jsonData) {
        this.jsonData = jsonData;
    }

    public LocalDate getCachedAt() {
        return cachedAt;
    }

    public void setCachedAt(LocalDate cachedAt) {
        this.cachedAt = cachedAt;
    }
}

